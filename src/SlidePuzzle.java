   private void startTimer() {
    timeRemaining = maximumTime;
    timer = new Timer(1000, new ActionListener() {

        @Override
        public void actionPerformed(ActionEvent e) {

            timeRemaining--;
            int minutes = timeRemaining / 60;
            int seconds = timeRemaining % 60;
            jLabel4.setText(String.format("%02d:%02d", minutes, seconds));

            if (timeRemaining <= 0) {
                timer.stop();
                if (!win) { // If the game is not won
                    loseSound();
                    dispose();
                    new GameOver("You ran out of time").setVisible(true);
                }
            }
        }
    });
    timer.start();
}


