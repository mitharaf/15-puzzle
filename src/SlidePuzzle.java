    boolean win = false;

    private void checkWin() {

        win = true;
        for (int i = 0; i < listOfButtons.length - 1; i++) {
            int expectedValue = i + 1;
            String buttonText = listOfButtons[i].getText();
            if (!buttonText.equals(String.valueOf(expectedValue))) {
                win = false;
                break;
            }
        }
        if (win) {
            InsertData.insert(playerName, counter, secondsPassed);
            dispose();
            new WinWindow(counter, secondsPassed).setVisible(win);
        } else if (counter > maxMoves && type == 'm') {

            loseSound();
            dispose();
            new GameOver("OUT OF MOVES").setVisible(true);
        }
    }

