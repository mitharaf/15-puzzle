    public void move(JButton clickedButton, JButton adjacentButton) {
        if (adjacentButton.getText().trim().isEmpty()) {
            String temp = clickedButton.getText();
            adjacentButton.setText(temp);
            clickedButton.setText("");
            counter = counter + 1;
            jLabel1.setText(String.valueOf(counter));
            checkWin();
            popSound();
        }
    }

    private void shuffle() {
        do {
            ArrayList<JButton> buttonList = new ArrayList<>(Arrays.asList(listOfButtons));
            Collections.shuffle(buttonList);
            for (int i = 0; i < buttonList.size(); i++) {
                JButton button = buttonList.get(i);
                button.setText(i == 0 ? "" : String.valueOf(i));
            }
        } while (!isSolvable());
//move(jButton15, jButton16);
    }

    private boolean isSolvable() {
        int inversions = 0;
        for (int i = 0; i < listOfButtons.length; i++) {
            for (int j = i + 1; j < listOfButtons.length; j++) {
                if (!listOfButtons[i].getText().equals("") && !listOfButtons[j].getText().equals("")) {
                    int val1 = Integer.parseInt(listOfButtons[i].getText());
                    int val2 = Integer.parseInt(listOfButtons[j].getText());
                    if (val1 > val2) {
                        inversions++;
                    }
                }
            }
        }
        return inversions % 2 == 0;
    }

